  TextStyle _createFont(
    TextStyle style, {
    required double sizePx,
    double? heightPx,
    double? spacingPc,
    FontWeight? weight,
    Color? color,
  }) {
    var szpx = sizePx;
    szpx *= _scale;
    double? hpx;
    if (heightPx != null) {
      hpx = heightPx * _scale;
    }
    return style.copyWith(
      fontSize: szpx,
      height: heightPx != null ? (hpx! / sizePx) : style.height,
      letterSpacing:
          spacingPc != null ? sizePx * spacingPc * 0.01 : style.letterSpacing,
      fontWeight: weight,
      color: color,
    );
  }
